n = int(input())
roof = (n + 1) // 2


for i in range(1, roof + 1):
    if n % 2 != 0:
        if i == 1:
            print((((n - 1) // 2) * "-") + i * "*" + (((n - 1) // 2) * "-"))
        else:
            print((((n - 2 * (i - 1)) // 2) * "-") + "*" + (i * 2 - 3) * "-" + "*" + (((n - 2 * (i - 1)) // 2) * "-"))
    if n % 2 == 0:
        if i == 1:
            print((((n - 1) // 2) * "-") + i * "**" + (((n - 1) // 2) * "-"))
        else:
            print((((n - 2 * i) // 2) * "-") + "*" + (i * 2 - 2) * "-" + "*" + (((n - 2 * i) // 2) * "-"))
for j in range(roof - 1, 0, -1):
    if n % 2 != 0:
        if j == 1:
            print((((n - 1) // 2) * "-") + j * "*" + (((n - 1) // 2) * "-"))
        else:
            print((((n - 2 * (j - 1)) // 2) * "-") + "*" + (j * 2 - 3) * "-" + "*" + (((n - 2 * (j - 1)) // 2) * "-"))
    if n % 2 == 0:
        if j == 1:
            print((((n - 1) // 2) * "-") + j * "**" + (((n - 1) // 2) * "-"))
        else:
            print((((n - 2 * j) // 2) * "-") + "*" + (j * 2 - 2) * "-" + "*" + (((n - 2 * j) // 2) * "-"))
